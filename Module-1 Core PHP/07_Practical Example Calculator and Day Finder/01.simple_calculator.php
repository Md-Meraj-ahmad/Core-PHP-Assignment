<?php
    $num1 = 10;
    $num2 = 5;
    $operator = '+';

    if ($operator == '+') {
    echo $num1 + $num2;
    } elseif ($operator == '-') {
    echo $num1 - $num2;
    } elseif ($operator == '*') {
    echo $num1 * $num2;
    } elseif ($operator == '/') {
    echo $num1 / $num2;
    }
?>
