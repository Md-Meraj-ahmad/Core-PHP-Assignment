<?php
// Concatenation
$str1 = "Hello, ";
$str2 = "World!";
echo "Concatenated String: " . $str1 . $str2 . "<br>";

// Substring extraction
$sentence = "Hello, World!";
echo "Substring (0, 5): " . substr($sentence, 0, 5) . "<br>";

// String length determination
echo "Length of 'Hello, World!': " . strlen($sentence) . "<br>";
?>
