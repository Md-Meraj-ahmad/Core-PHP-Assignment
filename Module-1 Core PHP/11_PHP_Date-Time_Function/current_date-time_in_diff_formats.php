<?php
// Current date in Y-m-d format
echo "Current Date (Y-m-d): " . date("Y-m-d") . "\n";

// Current time in H:i:s format
echo "Current Time (H:i:s): " . date("H:i:s") . "\n";

// Full date and time
echo "Full Date and Time: " . date("l, F j, Y g:i A") . "\n";

// Unix timestamp
echo "Unix Timestamp: " . time() . "\n";

// Date and time in custom format
echo "Custom Format (d/m/Y H:i): " . date("d/m/Y H:i") . "\n";
?>
